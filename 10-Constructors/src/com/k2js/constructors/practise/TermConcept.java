package com.k2js.constructors.practise;

class TermConcept{
	public static void main(String...xxyy){
		System.out.println(1000+2000);
	}
}

class TermConcept1{
	public static void main(String...xxyy){
		int i=1000,j=2000;
		System.out.println(i+j);
	}
}

class TermConcept2{
	public static void main(String...xxyy){
		final int i=1000,j=2000;
		System.out.println(i+j);
	}
}